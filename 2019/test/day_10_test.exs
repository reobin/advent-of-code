defmodule AoC.Day10Test do
  use ExUnit.Case
  doctest AoC.Day10

  alias AoC.Day10

  describe "part 1" do
    test "sample 1" do
      input = ".#..#
.....
#####
....#
...##"

      assert Day10.part_1(input) == 8
    end

    test "sample 2" do
      input = "......#.#.
#..#.#....
..#######.
.#.#.###..
.#..#.....
..#....#.#
#..#....#.
.##.#..###
##...#..#.
.#....####"

      assert Day10.part_1(input) == 33
    end

    test "sample 3" do
      input = "#.#...#.#.
.###....#.
.#....#...
##.#.#.#.#
....#.#.#.
.##..###.#
..#...##..
..##....##
......#...
.####.###."

      assert Day10.part_1(input) == 35
    end

    test "sample 4" do
      input = ".#..#..###
####.###.#
....###.#.
..###.##.#
##.##.#.#.
....###..#
..#.#..#.#
#..#.#.###
.##...##.#
.....#.#.."

      assert Day10.part_1(input) == 41
    end

    test "sample 5" do
      input = ".#..##.###...#######
##.############..##.
.#.######.########.#
.###.#######.####.#.
#####.##.#.##.###.##
..#####..#.#########
